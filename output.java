    public String show_out(String out){
        String s="";
    for (int i = 0; i < out.length(); i+=32) {
        if(out.length()<32){return out;}
        try{
        s+=out.substring(i,i+32)+"\n";
        }
        catch(Exception e){
        s+=out.substring(i);
        }
        }
    return s;
    } 
